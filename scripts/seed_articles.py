#!/usr/bin/env python3
"""一次性写入5篇首日文章到索引"""
import json, os, re, markdown
from datetime import datetime

ARTICLES_DIR = os.path.join(os.path.dirname(os.path.abspath(__file__)), "..", "articles")
ARTICLES_DIR = os.path.abspath(ARTICLES_DIR)

def add_article(title, seo_title, summary, keywords, tags, category, body_md):
    today = datetime.now()
    today_str = today.strftime("%Y-%m-%d")
    date_prefix = today_str.replace("-", "")
    slug = re.sub(r'[^\w\u4e00-\u9fff-]', '-', title[:30]).strip('-')
    slug = re.sub(r'-+', '-', slug)
    filename = f"{date_prefix}-{slug}"

    body_html = markdown.markdown(body_md, extensions=['tables', 'fenced_code'])

    entry = {
        "filename": filename,
        "title": title,
        "seo_title": seo_title,
        "summary": summary,
        "keywords": keywords,
        "tags": tags,
        "category": category,
        "date": today_str,
        "date_display": today.strftime("%Y年%m月%d日"),
        "date_iso": today.isoformat(),
        "date_rfc": today.strftime("%a, %d %b %Y %H:%M:%S +0800"),
        "body_html": body_html,
        "body_markdown": body_md,
    }

    index_path = os.path.join(ARTICLES_DIR, "_index.json")
    if os.path.exists(index_path):
        with open(index_path, "r", encoding="utf-8") as f:
            index = json.load(f)
    else:
        index = []

    index.insert(0, entry)

    with open(index_path, "w", encoding="utf-8") as f:
        json.dump(index, f, ensure_ascii=False, indent=2)

    print(f"  Added: {title[:40]}... -> {filename}")
    return filename

